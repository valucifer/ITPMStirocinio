package it.unisa.tirocinio.beans;

/**
 *
 * @author Valentino
 */
public class Details {
    private String detailsDescription = null;
    private String detailsTitle = null;
    private String detailsOrganization = null;
    private String detailsProfessor = null;
    private String detailsStudent = null;
    private String detailsTraining = null;
    private String detailsDepartment = null;
    private String detailsAccount = null;

    public Details() {
    }

    public String getDetailsTitle() {
        return detailsTitle;
    }

    public void setDetailsTitle(String detailsTitle) {
        this.detailsTitle = detailsTitle;
    }

    public void setDetailsDescription(String detailsDescription) {
        this.detailsDescription = detailsDescription;
    }

    public void setDetailsOrganization(String detailsOrganization) {
        this.detailsOrganization = detailsOrganization;
    }

    public void setDetailsProfessor(String detailsProfessor) {
        this.detailsProfessor = detailsProfessor;
    }

    public void setDetailsStudent(String detailsStudent) {
        this.detailsStudent = detailsStudent;
    }

    public void setDetailsTraining(String detailsTraining) {
        this.detailsTraining = detailsTraining;
    }

    public void setDetailsDepartment(String detailsDepartment) {
        this.detailsDepartment = detailsDepartment;
    }

    public void setDetailsAccount(String detailsAccount) {
        this.detailsAccount = detailsAccount;
    }

    public String getDetailsDescription() {
        return detailsDescription;
    }

    public String getDetailsOrganization() {
        return detailsOrganization;
    }

    public String getDetailsProfessor() {
        return detailsProfessor;
    }

    public String getDetailsStudent() {
        return detailsStudent;
    }

    public String getDetailsTraining() {
        return detailsTraining;
    }

    public String getDetailsDepartment() {
        return detailsDepartment;
    }

    public String getDetailsAccount() {
        return detailsAccount;
    }
    
    
    
}
